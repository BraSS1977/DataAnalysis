import pandas as pd

data = {
    'Ученик': [f'Ученик {i}' for i in range(1, 11)],
    'Математика': [5, 4, 3, 5, 4, 2, 5, 4, 3, 5],
    'Физика': [4, 3, 4, 5, 2, 4, 5, 3, 4, 4],
    'Химия': [3, 5, 4, 2, 4, 3, 5, 4, 3, 5],
    'Биология': [5, 4, 3, 4, 5, 4, 3, 5, 4, 3],
    'Литература': [4, 5, 4, 3, 4, 5, 4, 3, 5, 4]
}

df = pd.DataFrame(data).set_index('Ученик')

#Первые 5 строк
print("Первые 5 строк данных:")
print(df.head(), end="\n\n")

#Средняя оценка по предметам
print("Средние оценки:")
print(df.mean())
print("\n")

#Медианные оценки
print("Медианные оценки:")
print(df.median())
print("\n")

#Квантили
print("Квантили:")
Q1_math = df['Математика'].quantile(0.25)
Q3_math = df['Математика'].quantile(0.75)
IQR_math = Q3_math - Q1_math

print(f"Q1 по математике: {Q1_math}")
print(f"Q3 по математике: {Q3_math}")
print(f"IQR по математике: {IQR_math}")
print("\n")

#Стандартное отклонение
print("Стандартное отклонение:")
print(df.std())